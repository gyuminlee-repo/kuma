/**
 * compute-sidebar-width.mjs
 *
 * Build-time script: reads all subStep labels from i18n locale files,
 * estimates max pixel width using empirical char-width heuristic (no DOM),
 * and emits src/lib/sidebar-default-width.ts.
 *
 * Inter 14px medium: approx 7.3 px per character (±5 px vs DOM, spec §15.12).
 * Actual locale path: src/locales/{ko,en}.json (not src/i18n/locales/).
 *
 * Usage:
 *   node scripts/compute-sidebar-width.mjs
 *   (also runs as prebuild via package.json "compute:sidebar-width" script)
 */

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));

// Empirical average char width (px) for Inter 14px medium.
// ASCII/Latin: ~7.3 px/char. CJK (Korean): ~14 px/char (full-width glyphs).
// Validated against DOM measurement with ±5 px tolerance (spec §15.12).
const CHAR_WIDTH_LATIN = 7.3;
const CHAR_WIDTH_CJK = 14;

/** Check if a code point is CJK (Korean, Chinese, Japanese). */
function isCjk(cp) {
  return (
    (cp >= 0xac00 && cp <= 0xd7a3) || // Hangul syllables
    (cp >= 0x4e00 && cp <= 0x9fff) || // CJK unified
    (cp >= 0x3040 && cp <= 0x30ff)    // Hiragana/Katakana
  );
}

/**
 * Estimate pixel width of a single label string.
 * Uses per-character CJK vs Latin heuristic.
 *
 * @param {string} label
 * @param {number} scale  fontSize / 14
 * @returns {number}
 */
function labelWidth(label, scale) {
  let w = 0;
  for (const ch of label) {
    const cp = ch.codePointAt(0) ?? 0;
    w += isCjk(cp) ? CHAR_WIDTH_CJK : CHAR_WIDTH_LATIN;
  }
  return w * scale;
}

/**
 * Estimate maximum pixel width for a list of labels.
 *
 * @param {string[]} labels
 * @param {{ fontSize?: number; padding?: number; fallback?: number }} options
 * @returns {number}
 */
export function computeMaxLabelWidth(
  labels,
  { fontSize = 14, padding = 44, fallback = 240 } = {},
) {
  if (!labels.length) return fallback;
  const scale = fontSize / 14;
  const max = Math.max(...labels.map((l) => labelWidth(l, scale)));
  // Ensure result is at least the drag-handle min (180 px, spec §15.5).
  return Math.max(180, Math.ceil(max + padding));
}

/**
 * Recursively collect all string values from a nested object.
 * @param {unknown} obj
 * @returns {string[]}
 */
function collectStrings(obj) {
  const out = [];
  if (!obj || typeof obj !== "object") return out;
  for (const v of Object.values(obj)) {
    if (typeof v === "string") out.push(v);
    else if (v && typeof v === "object") out.push(...collectStrings(v));
  }
  return out;
}

/**
 * Read subStep labels from a locale JSON file.
 * Keys read: phaseC.subSteps.*, mame.subSteps.*
 *
 * @param {string} localePath absolute path to *.json
 * @returns {string[]}
 */
function collectSubStepLabels(localePath) {
  const data = JSON.parse(fs.readFileSync(localePath, "utf-8"));
  const labels = [];
  if (data.phaseC?.subSteps) labels.push(...collectStrings(data.phaseC.subSteps));
  if (data.mame?.subSteps) labels.push(...collectStrings(data.mame.subSteps));
  return labels;
}

function main() {
  const localesDir = path.join(__dirname, "..", "src", "locales");
  const labels = ["ko.json", "en.json"].flatMap((f) =>
    collectSubStepLabels(path.join(localesDir, f)),
  );

  const width = computeMaxLabelWidth(labels);
  const outPath = path.join(__dirname, "..", "src", "lib", "sidebar-default-width.ts");

  fs.writeFileSync(
    outPath,
    `// Auto-generated by scripts/compute-sidebar-width.mjs — do not edit.\n` +
      `// Recalculate: pnpm compute:sidebar-width\n` +
      `// Heuristic: Inter 14px medium, ~7.3 px/char ±5 px (spec §15.12).\n` +
      `export const SIDEBAR_DEFAULT_WIDTH = ${width};\n`,
    "utf-8",
  );

  console.log(`SIDEBAR_DEFAULT_WIDTH = ${width}  (${labels.length} labels from ko+en)`);
}

// Only run main() when executed directly, not when imported as a module.
const isMain =
  process.argv[1] &&
  path.resolve(process.argv[1]) === path.resolve(fileURLToPath(import.meta.url));

if (isMain) main();
