"""Unit tests for kuma_core.mame.ingest.combinatorial_demux.

Coverage:
- _extract_barcode_prefix: tail present / tail absent fallback
- load_barcodes: F/R prefix extraction from xlsx-like data
- _demux_read: exact-match fwd, exact-match RC, ambiguous drop, no-match drop
- run_combinatorial_demux: end-to-end with synthetic FASTQ + synthetic reference

Synthetic data strategy
-----------------------
Reference: 60 bp DNA (short enough to build synthetic reads quickly).
Reads: generated by embedding known F/R barcode prefixes around an amplicon
       that aligns to the reference at full span.
"""

