@pytest.fixture()
def mock_barcodes_xlsx(tmp_path: Path) -> Path:
    """Write a minimal xlsx that load_barcodes can read."""
    try:
        import openpyxl
    except ImportError:
        pytest.skip("openpyxl not available")

    wb = openpyxl.Workbook()
    ws = wb.active
    assert ws is not None, "Workbook has no active sheet"
    f_tail = "cacaggaggttaaacc"
    r_tail = "tgcgttgcgctctag"

    for i, bc in enumerate(_F_BARCODES, start=1):
        ws.append([f"isps_f_{i}", bc.lower() + f_tail])
    for i, bc in enumerate(_R_BARCODES, start=1):
        ws.append([f"isps_r_{i}", bc.lower() + r_tail])

    path = tmp_path / "barcodes.xlsx"
    wb.save(path)
    return path


def _make_fastq_gz(tmp_path: Path, reads: list[tuple[str, str]]) -> Path:
    """Write a gzipped FASTQ file and return its path."""
    fastq_path = tmp_path / "reads.fastq.gz"
    with gzip.open(fastq_path, "wt") as fh:
        for read_id, seq in reads:
            qual = "I" * len(seq)
            fh.write(f"@{read_id}\n{seq}\n+\n{qual}\n")
    return fastq_path


_F_TAIL = "cacaggaggttaaacc"
_R_TAIL = "tgcgttgcgctctag"


def _build_read(r_idx: int, f_idx: int, amplicon: str) -> str:
    """Build a synthetic read: R_barcode+tail + amplicon + F_barcode+tail (1-indexed).

    Tails are included so that load_barcodes (now returning full sequences)
    can find them as substrings.
    """
    return _R_BARCODES[r_idx - 1] + _R_TAIL + amplicon + _F_BARCODES[f_idx - 1] + _F_TAIL


