# Full-sequence barcode lists as returned by load_barcodes after this change.
# Each entry = barcode prefix + annealing tail (uppercase).
_F_BARCODES_FULL = [bc + _F_TAIL.upper() for bc in _F_BARCODES]
_R_BARCODES_FULL = [bc + _R_TAIL.upper() for bc in _R_BARCODES]


class TestDemuxRead:
    def test_exact_fwd_match(self) -> None:
        # Read contains full F/R sequences (prefix + tail)
        seq = _R_BARCODES[0] + _R_TAIL + _REF_SEQ + _F_BARCODES[0] + _F_TAIL
        result = _demux_read(seq, _F_BARCODES_FULL, _R_BARCODES_FULL)
        assert result == (1, 1)

    def test_rc_match(self) -> None:
        # Build a read where R1 + REF + F2 appear; take RC and confirm demux
        inner = _R_BARCODES[0] + _R_TAIL + _REF_SEQ + _F_BARCODES[1] + _F_TAIL
        seq = _reverse_complement(inner)
        result = _demux_read(seq, _F_BARCODES_FULL, _R_BARCODES_FULL)
        # Searching RC(seq) == inner finds R1 and F2
        assert result == (1, 2)

    def test_no_r_barcode_returns_none(self) -> None:
        seq = _REF_SEQ + _F_BARCODES[0] + _F_TAIL
        result = _demux_read(seq, _F_BARCODES_FULL, _R_BARCODES_FULL)
        assert result is None

    def test_no_f_barcode_returns_none(self) -> None:
        seq = _R_BARCODES[0] + _R_TAIL + _REF_SEQ
        result = _demux_read(seq, _F_BARCODES_FULL, _R_BARCODES_FULL)
        assert result is None

    def test_two_r_barcodes_returns_none(self) -> None:
        seq = _R_BARCODES[0] + _R_TAIL + _R_BARCODES[1] + _R_TAIL + _REF_SEQ + _F_BARCODES[0] + _F_TAIL
        result = _demux_read(seq, _F_BARCODES_FULL, _R_BARCODES_FULL)
        assert result is None

    def test_correct_1based_index(self) -> None:
        seq = _R_BARCODES[2] + _R_TAIL + _REF_SEQ + _F_BARCODES[4] + _F_TAIL
        result = _demux_read(seq, _F_BARCODES_FULL, _R_BARCODES_FULL)
        assert result == (3, 5)


# ---------------------------------------------------------------------------
# Integration tests: run_combinatorial_demux (synthetic end-to-end)
# ---------------------------------------------------------------------------


class TestRunCombinatorialDemux:
    def test_assigned_reads_and_wells(
        self, tmp_path: Path, tmp_reference: Path, mock_barcodes_xlsx: Path
    ) -> None:
        reads: list[tuple[str, str]] = []
        for i in range(5):
            reads.append((f"read_1_1_{i}", _build_read(1, 1, _REF_SEQ)))
        for i in range(3):
            reads.append((f"read_2_3_{i}", _build_read(2, 3, _REF_SEQ)))

        fastq = _make_fastq_gz(tmp_path, reads)
        out_dir = tmp_path / "out"

        result = run_combinatorial_demux(
            raw_fastq_paths=[fastq],
            reference_fasta=tmp_reference,
            barcodes_xlsx=mock_barcodes_xlsx,
            output_dir=out_dir,
            mapq_threshold=0,
            coverage_fraction=0.5,
            trim_flank_bp=30,
            min_depth=1,
        )

        assert result.stats.total_reads == 8
        assert result.stats.assigned_reads >= 1
        assert "1_1" in result.per_well_reads
        assert len(result.per_well_reads["1_1"]) >= 1

    def test_output_fasta_written(
        self, tmp_path: Path, tmp_reference: Path, mock_barcodes_xlsx: Path
    ) -> None:
        reads = [("r0", _build_read(1, 1, _REF_SEQ))]
        fastq = _make_fastq_gz(tmp_path, reads)
        out_dir = tmp_path / "out"

        run_combinatorial_demux(
            raw_fastq_paths=[fastq],
            reference_fasta=tmp_reference,
            barcodes_xlsx=mock_barcodes_xlsx,
            output_dir=out_dir,
            mapq_threshold=0,
            coverage_fraction=0.5,
            trim_flank_bp=30,
            min_depth=1,
        )

        fasta_files = list(out_dir.glob("*.fasta"))
        assert len(fasta_files) >= 1

    def test_consensus_dir_created(
        self, tmp_path: Path, tmp_reference: Path, mock_barcodes_xlsx: Path
    ) -> None:
        reads = [("r0", _build_read(1, 1, _REF_SEQ))]
        fastq = _make_fastq_gz(tmp_path, reads)
        out_dir = tmp_path / "out"

        run_combinatorial_demux(
            raw_fastq_paths=[fastq],
            reference_fasta=tmp_reference,
            barcodes_xlsx=mock_barcodes_xlsx,
            output_dir=out_dir,
            mapq_threshold=0,
            coverage_fraction=0.5,
            trim_flank_bp=30,
            min_depth=1,
        )

        assert (out_dir / "consensus").is_dir()

    def test_unassigned_reads_not_in_wells(
        self, tmp_path: Path, tmp_reference: Path, mock_barcodes_xlsx: Path
    ) -> None:
        reads = [("bare_read", _REF_SEQ)]
        fastq = _make_fastq_gz(tmp_path, reads)
        out_dir = tmp_path / "out"

        result = run_combinatorial_demux(
            raw_fastq_paths=[fastq],
            reference_fasta=tmp_reference,
            barcodes_xlsx=mock_barcodes_xlsx,
            output_dir=out_dir,
            mapq_threshold=0,
            coverage_fraction=0.5,
            trim_flank_bp=30,
            min_depth=1,
        )

        assert result.stats.assigned_reads == 0
        assert len(result.per_well_reads) == 0
