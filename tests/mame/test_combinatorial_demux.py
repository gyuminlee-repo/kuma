        assert f_bc[0] == "AATCCCACTACCACAGGAGGTTAAACC"   # F1
        assert f_bc[11] == "GAACATACGGCACAGGAGGTTAAACC"   # F12
        assert r_bc[0] == "CCCTATGACATGCGTTGCGCTCTAG"    # R1
        assert r_bc[7] == "AGAGTGCGGCTGCGTTGCGCTCTAG"    # R8


