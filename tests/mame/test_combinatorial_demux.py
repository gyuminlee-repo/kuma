class TestDemuxReadAnchored:
    def _make_aln_coords(self, read: str, amplicon: str) -> tuple[int, int]:
        """Return (q_st, q_en) for amplicon within read (exact substring)."""
        idx = read.find(amplicon)
        assert idx >= 0, "amplicon not found in read"
        return idx, idx + len(amplicon)

    def test_forward_strand_exact(self) -> None:
        read = _build_read(1, 1, _REF_SEQ)  # R1+Rtail+REF+F1+Ftail
        q_st, q_en = self._make_aln_coords(read, _REF_SEQ)
        result = _demux_read_anchored(
            read_seq=read, q_st=q_st, q_en=q_en, strand=1,
            r_barcodes=_R_BC_TUPLES, f_barcodes=_F_BC_TUPLES,
        )
        assert result == (1, 1)

    def test_forward_strand_r3_f5(self) -> None:
        read = _build_read(3, 5, _REF_SEQ)
        q_st, q_en = self._make_aln_coords(read, _REF_SEQ)
        result = _demux_read_anchored(
            read_seq=read, q_st=q_st, q_en=q_en, strand=1,
            r_barcodes=_R_BC_TUPLES, f_barcodes=_F_BC_TUPLES,
        )
        assert result == (3, 5)

    def test_minus_strand_rc_normalised(self) -> None:
        # Build +strand read, RC it, report strand=-1 and mappy-remapped coords
        fwd_read = _build_read(2, 4, _REF_SEQ)
        L = len(fwd_read)
        rc_read = _reverse_complement(fwd_read)
        # mappy q_st/q_en on rc_read: symmetric remap
        fwd_q_st, fwd_q_en = self._make_aln_coords(fwd_read, _REF_SEQ)
        # mappy on rc_read gives q_st = L - fwd_q_en, q_en = L - fwd_q_st
        rc_q_st = L - fwd_q_en
        rc_q_en = L - fwd_q_st
        result = _demux_read_anchored(
            read_seq=rc_read, q_st=rc_q_st, q_en=rc_q_en, strand=-1,
            r_barcodes=_R_BC_TUPLES, f_barcodes=_F_BC_TUPLES,
        )
        assert result == (2, 4)

    def test_one_mismatch_in_r_barcode_still_matches(self) -> None:
        # Introduce 1 mismatch in R1 prefix (10 bp, threshold=1 edit)
        # R1=CCCTATGACA; change pos6 G->T: CCCTATTACA (1 real mismatch)
        r_prefix = _R_BARCODES[0]
        mutated_r = r_prefix[:6] + "T" + r_prefix[7:]  # 1 mismatch (G->T at pos6)
        # Real library: F+FTAIL + amplicon + RC(RTAIL) + RC(mutated_r)
        read = (
            _F_BARCODES[0] + _F_TAIL
            + _REF_SEQ
            + _reverse_complement(_R_TAIL.upper())
            + _reverse_complement(mutated_r)
        )
        q_st, q_en = self._make_aln_coords(read, _REF_SEQ)
        result = _demux_read_anchored(
            read_seq=read, q_st=q_st, q_en=q_en, strand=1,
            r_barcodes=_R_BC_TUPLES, f_barcodes=_F_BC_TUPLES,
        )
        assert result == (1, 1)

    def test_one_mismatch_in_f_barcode_still_matches(self) -> None:
        # F2=TGAACTGAGCG (11 bp, threshold=1); change pos5 T->A: TGAACAGAGCG
        f_prefix = _F_BARCODES[1]
        mutated_f = f_prefix[:5] + "A" + f_prefix[6:]
        # Real library: mutated_f + FTAIL + amplicon + RC(RTAIL) + RC(R1)
        read = (
            mutated_f + _F_TAIL
            + _REF_SEQ
            + _reverse_complement(_R_TAIL.upper())
            + _reverse_complement(_R_BARCODES[0])
        )
        q_st, q_en = self._make_aln_coords(read, _REF_SEQ)
        result = _demux_read_anchored(
            read_seq=read, q_st=q_st, q_en=q_en, strand=1,
            r_barcodes=_R_BC_TUPLES, f_barcodes=_F_BC_TUPLES,
        )
        assert result == (1, 2)

    def test_no_barcode_returns_none(self) -> None:
        # Read with only amplicon, no barcodes in windows
        read = "A" * 30 + _REF_SEQ + "A" * 30
        q_st, q_en = self._make_aln_coords(read, _REF_SEQ)
        result = _demux_read_anchored(
            read_seq=read, q_st=q_st, q_en=q_en, strand=1,
            r_barcodes=_R_BC_TUPLES, f_barcodes=_F_BC_TUPLES,
        )
        assert result is None

    def test_ambiguous_barcodes_dropped(self) -> None:
        # Two F barcodes 1 edit apart; place a sequence equidistant from both in the 5-prime window
        # bc_a="AAAAAAAAAA", bc_b="AAAAAAAAAT" (1 edit apart)
        # "AAAAAAAAAC" is 1 edit from both -> tied -> ambiguous -> drop
        bc_a = "AAAAAAAAAA"
        bc_b = "AAAAAAAAAT"  # 1 edit from bc_a
        ambiguous_f = [("a", bc_a), ("b", bc_b)]
        # Place "AAAAAAAAAC" (1 edit from both bc_a and bc_b) in the F-window (5-prime end)
        # Real layout: ambiguous_f_seq + REF + RC(RTAIL) + RC(R1)
        read = (
            "AAAAAAAAAC"
            + _REF_SEQ
            + _reverse_complement(_R_TAIL.upper())
            + _reverse_complement(_R_BARCODES[0])
        )
        q_st, q_en = self._make_aln_coords(read, _REF_SEQ)
        result = _demux_read_anchored(
            read_seq=read, q_st=q_st, q_en=q_en, strand=1,
            r_barcodes=_R_BC_TUPLES, f_barcodes=ambiguous_f,
        )
        assert result is None  # ambiguous F -> drop


