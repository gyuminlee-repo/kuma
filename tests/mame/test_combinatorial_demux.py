class TestFindBestBarcode:
    def test_exact_match_returns_correct_index(self) -> None:
        window = "XXXX" + _R_BARCODES[2] + "YYYY"  # R3 exact
        result = _find_best_barcode(_R_BC_TUPLES, window, edit_dist_ratio=0.15)
        assert result is not None
        assert result[0] == 3  # 1-based index for R3
        assert result[1] == 0  # edit distance 0

    def test_one_mismatch_accepted(self) -> None:
        # Introduce one mismatch in R1 prefix (10 bp, threshold = int(10*0.15) = 1)
        bc = _R_BARCODES[0]  # CCCTATGACA
        mutated = bc[:6] + "T" + bc[7:]  # 1 mismatch (G->T at pos 6: CCCTATTACA)
        window = "GGG" + mutated + "AAA"
        result = _find_best_barcode(_R_BC_TUPLES, window, edit_dist_ratio=0.15)
        assert result is not None
        assert result[0] == 1  # R1
        assert result[1] == 1  # edit distance 1

    def test_two_mismatches_rejected(self) -> None:
        # 2 mismatches on a 10 bp barcode: threshold = floor(10*0.15) = 1 -> rejected
        bc = _R_BARCODES[0]
        mutated = "A" + bc[1:6] + "T" + bc[7:]  # 2 mismatches (C->A pos0, G->T pos6: ACCTATTACA)
        window = "GGG" + mutated + "AAA"
        result = _find_best_barcode(_R_BC_TUPLES, window, edit_dist_ratio=0.15)
        assert result is None

    def test_no_barcode_in_window_returns_none(self) -> None:
        result = _find_best_barcode(_R_BC_TUPLES, "AAAAAAAAAAAAAAAA", edit_dist_ratio=0.15)
        assert result is None

    def test_ambiguous_equal_edit_distance_dropped(self) -> None:
        # Create a window that matches two barcodes with the same edit distance.
        # Use a sequence equidistant (1 edit) from both R1 and R2.
        # R1=CCCTATGACA, R2=TAATGGCAAG -- these differ substantially;
        # instead use two synthetic barcodes differing by 1 edit and check ambiguity.
        bc_a = "AAAAAAAAAA"
        bc_b = "AAAAAAAAAC"  # 1 edit from bc_a
        barcodes = [("bc_a", bc_a), ("bc_b", bc_b)]
        # Window contains bc_a exactly -> bc_a dist=0, bc_b dist=1 -> unambiguous
        result = _find_best_barcode(barcodes, "XXX" + bc_a + "YYY", edit_dist_ratio=0.15)
        assert result is not None
        assert result[0] == 1  # bc_a wins

    def test_ambiguous_tied_drops(self) -> None:
        # Both barcodes match with the same edit distance -> ambiguous -> drop
        bc_a = "AAAAAAAAAA"
        bc_b = "AAAAAAAAAT"  # 1 edit from bc_a
        barcodes = [("bc_a", bc_a), ("bc_b", bc_b)]
        # Window contains "AAAAAAAAAТ" which has dist=1 from both bc_a and bc_b
        # Actually need window that is 1 edit from both:
        # "AAAAAAAAAC" -> dist to bc_a=1, dist to bc_b=1 (tie)
        result = _find_best_barcode(barcodes, "XXX" + "AAAAAAAAAC" + "YYY", edit_dist_ratio=0.15)
        assert result is None


