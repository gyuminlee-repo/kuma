# Short synthetic reference (60 bp)
_REF_SEQ = "ATGGCTTGCTCTGTATCCACTGAGAACGTATCTTTCACTGAGACTGAAACTGAGACCCGT"
# Barcode prefixes matching the xlsx structure (without annealing tails)
_F_BARCODES = [
    "AATCCCACTAC",  # F1
    "TGAACTGAGCG",  # F2
    "TATCTGACCTT",  # F3
    "ATATGAGACG",   # F4
    "CGCTCATTAG",   # F5
    "TAATCTCGTC",   # F6
    "GCGCGATTTT",   # F7
    "AGAGCACTAG",   # F8
    "TGCCTTGATC",   # F9
    "CTACTCAGTC",   # F10
    "TCGTCTGACT",   # F11
    "GAACATACGG",   # F12
]

_R_BARCODES = [
    "CCCTATGACA",  # R1
    "TAATGGCAAG",  # R2
    "AACAAGGCGT",  # R3
    "GTATGTAGAA",  # R4
    "TTCTATGGGG",  # R5
    "CCTCGCAACC",  # R6
    "TGGATGCTTA",  # R7
    "AGAGTGCGGC",  # R8
]


